package C.WhileLoop;

public class EvenNumbers
{
    public static void main(String[] args)
    {

        int x=1;
        int y=2;
        while(x<=10) {
            System.out.println("Odd numbers:" + x);
            x += 2;
        }
        while(y<=10) {
            System.out.println("Even number:" + y);
            y += 2;
        }




            }
        }


